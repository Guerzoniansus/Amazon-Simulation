    @Override
    public Graph getGraph() {

        Map<Integer, Node> nodes = loadNodes(TEXT_FILE);
        Graph graph = new Graph(nodes, DISTANCE_MATRIX);

        return graph;
    }

    /**
     * Load a list of nodes from a text file
     * @param textFile The path to the text file
     * @return A map with the Node ID as key and the Node as value
     */
    private Map<Integer, Node> loadNodes(String textFile) {

        try {
            File file = new File(textFile);
            BufferedReader reader = new BufferedReader(new FileReader(file));

            final Map<Integer, Node> nodes = new HashMap<>();

            String line = "";
            int lineCounter = 0;
            int nodeCounter = 0;

            while ((line = reader.readLine()) != null) {
                lineCounter++;

                line = line.replaceAll(" ", "");

                // Check for comment
                if (line.startsWith("/")) {
                    continue;
                }

                if (isValidString(line) == false) {
                    throw new IllegalArgumentException("Invalid format at line " + lineCounter + " of " + textFile
                    + " with message [" + line + "]");
                }

                Node newNode = parseNode(line);
                nodes.put(newNode.getID(), newNode);
                nodeCounter++;
            }

            reader.close();

            return nodes;
        }

        catch (IOException e) {
            IntStream.rangeClosed(0, 25)
                    .forEach(x -> System.out.println("ERROR: FILE NOT FOUND!"));
            System.exit(1);
        }

        return null;
    }

    /**
     * Create a new node by parsing its parameters from a string of text
     * @param text The text to parse
     * @return The newly created Node
     */
    private Node parseNode(String text) {
        // Format:
        // ID: TYPE, X, Z

        text = text.replaceAll(" ", "");

        String[] textSplit = text.split(":");

        // ======== ID
        int ID = Integer.parseInt(textSplit[0]);

        // Regular args
        String argSplit = textSplit[1];
        String[] args = argSplit.split(",");

        // ======== NodeType, X, Y
        NodeType nodeType = NodeType.valueOf(args[0].toUpperCase());
        double x = Double.parseDouble(args[1]);
        double z = Double.parseDouble(args[2]);

        Node node = new Node(ID, nodeType, x, NODE_Y, z);
        return node;
    }

    /**
     * Check if the given string is valid according to the assumed format
     * @param text The string to check
     * @return Whether the format of this string is valid or not
     */
    private boolean isValidString(String text) {
        // Format:
        // ID: TYPE, X, Z - NeighbourID=Distance (NeighbourID2=Distance... etc)

        if (StringUtils.countOccurrencesOf(text, ":") != 1) {
            return false;
        }

        if (StringUtils.countOccurrencesOf(text, ",") != 2) {
            return false;
        }

        try {
            Integer.parseInt(text.split(":")[0]);
        }

        catch (NumberFormatException e) {
            return false;
        }

        return true;
    }
